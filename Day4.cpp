// 2 stars

// My approach is iterating through the array, looking for matching substring of XMAS and SAMX
// to the right, bottom, diagonally bottom left and diagonally bottom right. This way, there
// are no duplicates if I'm starting from the top left of the array.

#include <iostream>
#include <vector>
#include <sstream>
#include <string>
using namespace std;

// Convert input to vector
void parse(const string& input, vector<string>& arr)
{
    string line;
    istringstream iss(input);
    // Seperate string by line
    while (getline(iss, line))
    {
        arr.push_back(line);
    }
}

int wordSearch(const vector<string>& arr)
{
    int result = 0;
    int arrWidth = arr[0].size();
    int arrHeight = arr.size();
    // Nested loop, iterate through 2D array
    for (int i = 0; i < arrHeight; i++)
    {
        for (int j = 0; j < arrWidth; j++)
        {
            // Right
            if (arr[i].substr(j, 4) == "XMAS" || arr[i].substr(j, 4) == "SAMX")
            {
                result++;
            }
            // Bottom
            if (i + 4 <= arrHeight)
            {
                string temp;
                for (int it = 0; it < 4; it++)
                {
                    temp.push_back(arr[i + it][j]);
                }
                if (temp == "XMAS" || temp == "SAMX")
                {
                    result++;
                }
            }
            // Diagonally left
            if (i + 4 <= arrHeight && j - 3 >= 0)
            {
                string temp;
                for (int it = 0; it < 4; it++)
                {
                    temp.push_back(arr[i + it][j - it]);
                }
                if (temp == "XMAS" || temp == "SAMX")
                {
                    result++;
                }
            }
            // Diagonally right
            if (i + 4 <= arrHeight && j + 4 <= arrWidth)
            {
                string temp;
                for (int it = 0; it < 4; it++)
                {
                    temp.push_back(arr[i + it][j + it]);
                }
                if (temp == "XMAS" || temp == "SAMX")
                {
                    result++;
                }
            }
        }
    }
    return result;
}

int wordSearchV2(const vector<string>& arr)
{
    // Same approach, this time only need to check for X shape
    int result = 0;
    int arrWidth = arr[0].size();
    int arrHeight = arr.size();
    for (int i = 0; i < arrHeight - 2; i++)
    {
        for (int j = 0; j < arrWidth - 2; j++)
        {
            string temp;
            temp.push_back(arr[i][j]);
            temp.push_back(arr[i][j + 2]);
            temp.push_back(arr[i + 1][j + 1]);
            temp.push_back(arr[i + 2][j]);
            temp.push_back(arr[i + 2][j + 2]);
            
            if (temp == "MSAMS" || temp == "SSAMM" || temp == "MMASS" || temp == "SMASM")
            {
                result++;
            }
        }
    }
    return result;
}


